            Herbert Bayer (Haag, Austria, 1900 - Santa Bárbara, Estados Unidos, 1985) fue un diseñador gráfico, pintor, fotógrafo y arquitecto austriaco. Bayer fue el diseñador de publicidad más innovador de la Escuela de la Bauhaus, a la que perteneció: su trabajo destaca por la temprana introducción de la fotografía en los años 1920.
            Bayer fue aprendiz del artista George Schmidthammer en Linz. Tras dejar el taller se interesó por el manifiesto Bauhaus de Walter Gropius. Tras estudiar durante cuatro años en la Escuela de la Bauhaus,1​ con profesores como Wassily Kandinsky2​ o László Moholy-Nagy, Gropius nombró a Bayer director de impresión y publicidad. Influenciado por el minimalismo, Bayer desarrolló un estilo visual nítido y adoptó una fuente sans serif completamente en minúsculas para todas las publicaciones de Bauhaus. En 1925 diseñó una fuente sans serif geométrica, universal, cuya versión digital recibe el nombre de Architype Bayer.
